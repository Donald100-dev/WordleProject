def command(text_input):
    if ":" not in text_input: return None
    command, val = text_input.split(":")
    command = command.lower().strip()
    val = val.lower().strip()
    
    if command == CODE_GET:
        val = "target language" if val == "language" else val
        if val == "texts": return None
        res = get_setting_game(val)
        for key in case_language:
            lang = case_language[key]
            if lang != res: continue
            res = key
            break
            
        print(" ", res, end=" ")
        if res[0] == "'":
            return None
        return True

    if command == CODE_SET_LANG:
        lang = autoTransEnglish(val).lower()
        lang = case_language.get(lang, None)
        if lang is None: return False
        set_setting_game("target language", lang)
        language_switch()
        return True

    if command == CODE_SET_LEVEL:
        level = val
        if not (level == "easy" or level == "hard"):
            return False
        set_setting_game("level", level)
        return True
    return None
        

